// 객체지향의 기본적 개념

/*
	POP - Procedual Oriented Programming
	OOP - Object Oriented Programming
*/

/*
	- Class
		객체의 설계도
	- Object
		클래스 타입으로 선언된것
	- Instance
		클래스를 실제 구현, 사용 가능한것
*/

/*
	객체지향(OOP)의 4대 특징

	- 추상화(Abstraction) : 모델링
		여러 대상을 다룰 때 이 대상들의 공통점을 이용해 클래스로 만들어서 사용하는 방법

	- 캡슐화(Encapsulation)
		객체가 구현된 방식의 세부정보를 사용자로부터 숨기면서 유지하는 방식
		: 정보은닉(Informaion hiding)

	- 상속(Inheritance)
		부모 클래스의 멤버 변수와 메소드를 자식 클래스가 재사용하는 개념
		: 재사용과 확장에 용이

	- 다형성(Polymorphism)
		: overloading : 같은 이름의 함수를 매개변수의 자료형이나 수를 달리 하여 생성하는 것.
		: overriding : 부모 클래스의 함수를 자식 클래스에서 재정의
*/